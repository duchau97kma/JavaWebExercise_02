package Buoi4_2;

/**
 *
 * @author Winter
 */
public class Polygon {
    private int top;
    private int edge;

    public Polygon() {
    }

    public Polygon(int top, int edge) {
        this.top = top;
        this.edge = edge;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getEdge() {
        return edge;
    }

    public void setEdge(int edge) {
        this.edge = edge;
    }
    public double getArea(){
        
        return getArea();
    }
    public double getPerimeter(){
        
        return getPerimeter();
    }
}
